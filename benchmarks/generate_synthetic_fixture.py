#!/usr/bin/env python3

import argparse
import math
from pathlib import Path


CASES = {
    "small": {"dims": (2, 2, 1), "n_steps": 100, "n_runs": 1, "energy_cutoff": 10.0},
    "medium": {"dims": (4, 4, 2), "n_steps": 1500, "n_runs": 2, "energy_cutoff": 12.0},
    "heavy": {"dims": (8, 8, 4), "n_steps": 5000, "n_runs": 3, "energy_cutoff": 15.0},
}


def energy_value(x: int, y: int, z: int, nx: int, ny: int, nz: int) -> float:
    # Synthetic corridor with periodic low barriers plus a few strong blockers.
    if x == 0 or y == 0:
        return 0.0 + 0.05 * ((z + x + y) % 3)
    if z == 0:
        return 0.2 + 0.05 * ((x + y) % 4)
    if (x + y + z) % 5 == 0:
        return 1.5
    if x == nx - 1 and y == ny - 1:
        return 9.5
    return 0.6 + 0.1 * ((x * 3 + y + z) % 5)


def write_input_param(path: Path, n_steps: int, n_runs: int, energy_cutoff: float) -> None:
    lines = [
        "1",
        "1",
        "300",
        "1",
        "0",
        str(n_steps),
        "1",
        str(n_runs),
        "1",
        "1",
        "1.0",
        "1.0",
        f"{energy_cutoff:.1f}",
    ]
    path.write_text("\n".join(lines) + "\n", encoding="ascii")


def write_grid_cube(path: Path, dims: tuple[int, int, int]) -> None:
    nx, ny, nz = dims
    values = []
    for x in range(nx):
        for y in range(ny):
            for z in range(nz):
                values.append(energy_value(x, y, z, nx, ny, nz))

    lines = [
        "synthetic benchmark",
        "generated by generate_synthetic_fixture.py",
        "0 0.0 0.0 0.0",
        f"{nx} 1.0 0.0 0.0",
        f"{ny} 0.0 1.0 0.0",
        f"{nz} 0.0 0.0 1.0",
    ]

    row = []
    for value in values:
        row.append(f"{value:.6f}")
        if len(row) == 6:
            lines.append(" ".join(row))
            row = []
    if row:
        lines.append(" ".join(row))

    path.write_text("\n".join(lines) + "\n", encoding="ascii")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--case", choices=sorted(CASES.keys()), required=True)
    parser.add_argument("--output-dir", required=True)
    args = parser.parse_args()

    spec = CASES[args.case]
    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)
    write_input_param(out_dir / "input.param", spec["n_steps"], spec["n_runs"], spec["energy_cutoff"])
    write_grid_cube(out_dir / "grid.cube", spec["dims"])


if __name__ == "__main__":
    main()
